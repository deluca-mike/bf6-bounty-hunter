function buildInitializeOptions(options: FFASpawnPoints.InitializeOptions): FFASpawnPoints.InitializeOptions {
    return Object.assign({ initialPromptDelay: 3, promptDelay: 10 }, options);
}

type SpawnDataAndInitializeOptions = {
    spawnData?: FFASpawnPoints.SpawnData[];
    spawnOptions?: FFASpawnPoints.InitializeOptions;
    dropInData?: FFADropIns.SpawnData;
};

export function getSpawnDataAndInitializeOptions(): SpawnDataAndInitializeOptions {
    const map = MapDetector.currentMap();

    if (map == MapDetector.Map.EmpireState) {
        return {
            spawnData: EMPIRE_STATE_SPAWNS,
            spawnOptions: buildInitializeOptions(EMPIRE_STATE_INITIALIZATION_OPTIONS),
        };
    }

    if (map == MapDetector.Map.Eastwood) {
        return {
            spawnData: EASTWOOD_SPAWNS,
            spawnOptions: buildInitializeOptions(EASTWOOD_INITIALIZATION_OPTIONS),
        };
    }

    if (map == MapDetector.Map.Downtown) {
        return {
            spawnData: DOWNTOWN_SPAWNS,
            spawnOptions: buildInitializeOptions(DOWNTOWN_INITIALIZATION_OPTIONS),
        };
    }

    if (map == MapDetector.Map.Marina) {
        return {
            spawnData: MARINA_SPAWNS,
            spawnOptions: buildInitializeOptions(MARINA_INITIALIZATION_OPTIONS),
        };
    }

    if (map == MapDetector.Map.Area22B) {
        return {
            spawnData: AREA_22B_SPAWNS,
            spawnOptions: buildInitializeOptions(AREA_22B_INITIALIZATION_OPTIONS),
        };
    }

    if (map == MapDetector.Map.IberianOffensive) {
        return {
            spawnData: IBERIAN_OFFENSIVE_SPAWNS,
            spawnOptions: buildInitializeOptions(IBERIAN_OFFENSIVE_INITIALIZATION_OPTIONS),
        };
    }

    if (map == MapDetector.Map.OperationFirestorm) {
        return {
            spawnOptions: buildInitializeOptions({}),
            dropInData: OPERATION_FIRESTORM_DROP_INS,
        };
    }

    if (map == MapDetector.Map.LiberationPeak) {
        return {
            spawnOptions: buildInitializeOptions({}),
            dropInData: LIBERATION_PEAK_DROP_INS,
        };
    }

    if (map == MapDetector.Map.BlackwellFields) {
        return {
            spawnOptions: buildInitializeOptions({}),
            dropInData: BLACKWELL_FIELDS_DROP_INS,
        };
    }

    if (map == MapDetector.Map.ManhattanBridge) {
        return {
            spawnOptions: buildInitializeOptions({}),
            dropInData: MANHATTAN_BRIDGE_DROP_INS,
        };
    }

    if (map == MapDetector.Map.NewSobekCity) {
        return {
            spawnOptions: buildInitializeOptions({}),
            dropInData: NEW_SOBEK_CITY_DROP_INS,
        };
    }

    if (map == MapDetector.Map.MirakValley) {
        return {
            spawnOptions: buildInitializeOptions({}),
            dropInData: MIRAK_VALLEY_DROP_INS,
        };
    }

    if (map == MapDetector.Map.GolfCourse) {
        return {
            spawnOptions: buildInitializeOptions({}),
            dropInData: GOLF_COURSE_DROP_INS,
        };
    }

    if (map == MapDetector.Map.DefenseNexus) {
        return {
            spawnOptions: buildInitializeOptions({}),
            dropInData: DEFENSE_NEXUS_DROP_INS,
        };
    }

    if (map == MapDetector.Map.RedlineStorage) {
        return {
            spawnOptions: buildInitializeOptions({}),
            dropInData: REDLINE_STORAGE_DROP_INS,
        };
    }

    if (map == MapDetector.Map.Contaminated) {
        return {
            spawnOptions: buildInitializeOptions({}),
            dropInData: CONTAMINATED_DROP_INS,
        };
    }

    return {};
}
